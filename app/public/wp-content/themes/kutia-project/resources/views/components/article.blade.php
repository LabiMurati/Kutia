               <h2>Solceller på taket – ikke vær for grønn!</h2>
               <div class="single-article__img">
                  <img src="@asset('images/Mask Group 2.png')" alt="">
               </div>
               <div class="single-article__content">
                  <span>4 NOVEMBER 2019</span>
                  <h3>Det blir stadig mer populært med solceller eller solfangere på både nye og eksisterende bygg. Dette er elektriske installasjoner, 
                     som kan forårsake brann, som anbefales hensyntatt. Ellers risikerer du å komme i ansvar! 
                  </h3>
               </div>
               <h4>Søknadspliktig tiltak?</h4>
               <p>Installasjoner av solenergianlegg er som hovedregel søknadspliktig. Reglene om saksbehandling og ansvarlige foretak omfatter derfor disse arbeidene. Uten søknad og tillatelse risikerer man pålegg fra byggesak om riving mv. De ansvarlige foretakene,
                  som byggmestere ofte er, må blant annet ivareta TEK17 sine krav til brannsikkerhet.
               </p>
               <p>Installering og endring av solenergianlegg i eksisterende byggverk,
                  innenfor en bruksenhet eller branncelle (som for eksempel enebolig) er imidlertid unntatt 
                  fra kravet om søknadsplikt. Selv om dette ikke er søknadspliktige arbeider, er byggherre 
                  likevel ansvarlig for at de materielle minimumskravene gjelder, som brannsikkerhet i bygninger.
               </p>
               <p>TEK17 krever at tekniske installasjoner skal prosjekteres og utføres slik at installasjoner ikke øker faren vesentlig for
                  at brann oppstår, eller at brann og røyk sprer seg. Byggmesteren bør derfor tipse byggherre om å involvere prosjekterende/utførende
                  også innen elektro og brann allerede på planleggingsstadiet.
                  Videre stiller TEK17 krav til tilrettelegging for redningsmannskaper, som også skal oppfylles som minimumskrav.
               </p>
               <p>Er man i tvil kan man kontakte byggesak i den aktuelle kommunen.</p>
